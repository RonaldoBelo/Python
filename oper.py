a = int(input("Digite um numero: "))
b = int(input("Digite outro numero: "))
soma = a + b
sub = a - b
mult = a * b
div = a / b
div_int = a // b
rest = a % b
exp = a ** b
print(f'Soma: {soma}')
print(f'Subtração: {sub}')
print(f'Multiplicação: {mult}')
print(f'Divisão: {div}')
print(f'Divisão Inteira: {div_int}')
print(f'Resto da Divisão: {rest}')
print(f'Exponenciação: {exp}')